import os
import sys

try:
    # Пытаемся импортировать функцию для загрузки переменных окружения из файла `.env`.
    from dotenv import load_dotenv
    # Пытаемся импортировать современный Python SDK для Gemini.
    from google import genai
    # Импортируем тип ошибок SDK, чтобы красиво обработать возможную ошибку API.
    from google.genai import errors
except ImportError as error:
    # Сообщаем, что нужные библиотеки еще не установлены.
    print("Не найдены библиотеки `python-dotenv` и/или `google-genai`.")
    # Показываем путь к текущему интерпретатору, чтобы было видно, какой Python реально запустил файл.
    print(f"Текущий интерпретатор: {sys.executable}")
    # Проверяем, есть ли в проекте локальное виртуальное окружение `.venv`.
    if os.path.exists(".venv/bin/python"):
        # Подсказываем самый надежный запуск через локальное окружение проекта.
        print("В проекте найдено локальное окружение `.venv`.")
        # Показываем команду прямого запуска через Python из `.venv`.
        print("Запустите так: ./.venv/bin/python week4.py")
        # Показываем команду активации окружения, если пользователь хочет работать внутри него.
        print("Или активируйте окружение: source .venv/bin/activate")
    # Показываем команду для установки пакетов именно в текущий интерпретатор Python.
    print("Если хотите установить пакеты в текущий Python, выполните:")
    print(f"{sys.executable} -m pip install python-dotenv google-genai")
    # Показываем альтернативу через uv для разового запуска без ручной установки.
    print("Или разовый запуск через uv:")
    print("uv run --with python-dotenv --with google-genai python week4.py")
    # Завершаем программу с кодом ошибки и сохраняем исходную причину сбоя импорта.
    raise SystemExit(1) from error

# Загружаем значения из файла `.env` в переменные окружения текущего процесса.
load_dotenv()

# Читаем API-ключ Gemini из переменной окружения `GEMINI_API_KEY`.
api_key = os.getenv("GEMINI_API_KEY")

# Проверяем, что ключ действительно найден после загрузки `.env`.
if not api_key:
    # Сообщаем, что в `.env` должен быть указан ключ Gemini.
    print("В файле `.env` не найден `GEMINI_API_KEY`.")
    # Показываем ожидаемый формат строки в `.env`.
    print("Добавьте строку вида: GEMINI_API_KEY=ваш_ключ")
    # Завершаем программу, потому что без ключа запрос выполнить нельзя.
    raise SystemExit(1)

# Сохраняем имя модели в переменную, чтобы его было проще поменять позже.
model_name = "gemini-2.5-flash"

# Сохраняем пользовательское сообщение в отдельную переменную.
user_message = "Что такое большая языковая модель? Объясни в двух предложениях"

try:
    # Создаем клиента Gemini и передаем ему API-ключ из `.env`.
    with genai.Client(api_key=api_key) as client:
        # Отправляем простой текстовый запрос модели и получаем объект ответа.
        response = client.models.generate_content(
            # Указываем, какую модель Gemini нужно вызвать.
            model=model_name,
            # Передаем текст пользовательского сообщения как содержимое запроса.
            contents=user_message,
        )

    # Печатаем заголовок перед текстом ответа модели.
    print("Ответ модели:")
    # Печатаем уже готовый текстовый ответ Gemini.
    print(response.text)

    # Сохраняем метаданные использования токенов в отдельную переменную для удобства.
    usage = response.usage_metadata

    # Проверяем, вернул ли SDK информацию о токенах.
    if usage:
        # Печатаем общее количество токенов во входе и выходе вместе.
        print(f"\nВсего токенов использовано: {usage.total_token_count}")
        # Печатаем количество токенов во входном сообщении.
        print(f"Токенов во входе: {usage.prompt_token_count}")
        # Печатаем количество токенов в ответе модели.
        print(f"Токенов в ответе: {usage.candidates_token_count}")
    else:
        # Сообщаем, что SDK не вернул статистику по токенам для этого ответа.
        print("\nSDK не вернул `usage_metadata` для этого ответа.")
except errors.APIError as error:
    # Обрабатываем ошибки Gemini API, например неверный ключ, лимиты или недоступную модель.
    print(f"Ошибка Gemini API: {error}")
